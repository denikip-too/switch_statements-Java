
package switch_statements;

public class Switch_Statements {

    public static void main(String[] args) {
        int number=60;
        switch (number)
        {
            case 10:
                System.out.println("Your choice is 10");
                break;
            case 20:
                System.out.println("Your choice is 20");
                break;
            case 30:
                System.out.println("Your choice is 30");
                break;
            case 40:
                System.out.println("Your choice is 40");
                break;
            case 50:
                System.out.println("Your choice is 50");
                break;
            default:
                System.out.println("Not in the range of 10,20,30,40,50");
        }
    }
    
}
